void PWM_Init(void)			//pwm频率为50Hz
{
    htim3.Instance = TIM3;
    htim3.Init.Prescaler = 72 - 1;       // 设置PSC
    htim3.Init.CounterMode = TIM_COUNTERMODE_UP;
    htim3.Init.Period = 20000 - 1;       //设置ARR，计算f=1MHz/20000 = 50Hz
    htim3.Init.ClockDivision = TIM_CLOCKDIVISION_DIV1;
    HAL_TIM_PWM_Init(&htim3);
    
    sConfigOC.OCMode = TIM_OCMODE_PWM1;
    sConfigOC.Pulse = 1500;              // 设置1.5ms脉宽
    sConfigOC.OCPolarity = TIM_OCPOLARITY_HIGH;
    sConfigOC.OCFastMode = TIM_OCFAST_DISABLE;
    HAL_TIM_PWM_ConfigChannel(&htim3, &sConfigOC, TIM_CHANNEL_2);//配置对应通道

    sConfigOC.Pulse = 1500;              // 设置1.5ms脉宽
    HAL_TIM_PWM_ConfigChannel(&htim3, &sConfigOC, TIM_CHANNEL_4);
    
    HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_2);			//启用pwm输出
    HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_4);
}
