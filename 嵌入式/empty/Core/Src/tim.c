void Set_Servo_Angle(TIM_HandleTypeDef *htim, uint32_t Channel, uint8_t angle)//设置转角
{
    angle = (angle > 180) ? 180 : angle;	//限位

    uint16_t pulse = 500 + angle * 2000 / 180;  	//通过调节脉宽来控制角度
    
    __HAL_TIM_SET_COMPARE(htim, Channel, pulse);//占空比更新
}


void Set_Two_Servos_Angle(uint8_t angle1, uint8_t angle2)		//设置输出转角
{
    Set_Servo_Angle(&htim3, TIM_CHANNEL_2, angle1);  // PB5
    Set_Servo_Angle(&htim3, TIM_CHANNEL_4, angle2);  // PB1
}
