void OLED_ShowCHinese_pointer(uint8_t x,uint8_t y,uint8_t no,unsigned char 
	add[][32])	//	şş×ÖĎÔĘľ
{      			    
	unsigned char (*c)[32]=add;
	uint8_t t,adder=0;
	for(int i=0;i<no;i++){
	oled_origin(x*8+i*16,y);	
    for(t=0;t<16;t++)
	{
		oled_data(*(*(c+2*i)+t));
		//adder+=1;
	}	
	oled_origin(x*8+i*16,y+1);	
    for(t=0;t<16;t++)
	{	
		oled_data(*(*(c+2*i+1)+t));
		//adder+=1;
	}					
	}
}
